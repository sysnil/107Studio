民革河南大学小组1953年3月成立，全国第三届人民代表大会代表、开封市副市长、民革开封市委副主委、河南大学文史系主任、图书馆馆长张邃青教授为负责人。“文化大革命”中停止活动，1979年恢复组织。

1983年民革河南大学支部成立，主委由陈际轩先生担任；第二届至第六届支部主委由钱衡副教授担任，徐泽源、苗润圃、赵全利等先后担任支部组织委员，杨麦龙教授担任宣传委员。

1996年5月、1999年5月，民革河南大学分别进行了支部换届，选举产生的第七届、第八届支部主任委员均由丁英俊教授。2002年4月换届产生的民革河南大学第九届支部委员会，丁英俊教授继续担任主委，管华副教授任副主委；2008年1月支部换届，丁英俊教授继续担任主委，赵全利副教授任副主委。刘大建、丁凯任支部委员。2013年11月21日，民革河南大学第六次党员大会召开，选举产生了民革河南大学支部第六届委员会，丁英俊任主委，张先飞任副主委，刘大建、丁凯任委员。

苗润圃副教授曾担任民革中央委员、民革河南省委常委，民革开封市委主委，开封市第八届、九届政协副主席，第十一届全国人大代表；张绍良教授曾任民革河南省委顾问；丁英俊教授担任民革河南省委委员、开封市委委员且为开封市第八届、九届、十届政协委员；刘大建为开封市第十届政协常委、第十一届政协委员。民革河大支部现有成员13人。
