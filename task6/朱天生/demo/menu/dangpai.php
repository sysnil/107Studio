						<h2 style="text-align: center;">党委统战部主要职责</h2>
						<br>
						统战部是校党委负责统一战线工作的职能部门，主要职责有：<br><br>
						   1.贯彻落实党的统一战线工作方针政策，团结引导各民主党派、团体和无党派人士自觉拥护中国共产党的领导，为国家建设和学校发展服务。<br><br>
						   2.支持校内各民主党派、团体独立自主地开展工作，协助其加强思想建设、组织建设。加强党外人士的教育和培训工作。<br><br>
						   3.宣传贯彻党的民族、宗教政策，开展民族团结工作，做好信教师生的教育引导工作，保护少数民族师生和信教师生的合法权益。<br><br>
						   4.开展海外统战工作，加强同港、澳、台和海外华侨、华人的联系，为经济建设和祖国统一大业做贡献。<br><br>
						   5.做好无党派知识分子和留学人员工作，充分发挥无党派知识分子和留学人员在学校“双一流”建设中的积极作用，凝聚人心、汇集力量，为国家和学校的发展献计出力。<br><br>
						   6. 负责党外人士的政治安排，会同有关部门做好党外干部的培养、考察、选拔、推荐，做好党外后备干部队伍的建设工作。<br><br>
						   7. 健全和落实学校党委与民主党派保持密切联系的各项制度。负责联系学校统一战线各界人士，组织召开情况通报会、座谈会、职能部门情况通报会，组织各类调研活动。<br><br>
